let message = 'Hello, Sunshine!';
console.log(message); // Output: Hello, Sunshine!

// You can join two or more strings as one with the plus + symbol.
let text = 'Hello ' + 'and ' + 'Goodbye!';
console.log(text);

// When printing a variable's value, you can also add strings in the `console.log()` function directly as follows:
let message1 = 'Hello, Dave!';
console.log('The message is: ' + message1);

let name = 'John';
let topic = 'JavaScript';
console.log(name + ' is learning ' + topic + ' today');

// You can also use template strings format
console.log(`${name} is learning ${topic} today`);

// Strings can also represent numbers. You surround the numbers in quotations as follows:
let score = '10' + '30';
console.log(score);
